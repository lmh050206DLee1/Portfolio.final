import turtle as t


screen= t.Screen()
screen.setup(1000,1000)

def name():
    t.penup()
    t.goto(300,300)
    t.pendown()
    t.right(-90)
    t.forward(100)
    t.right(125)
    t.forward(50)
    t.right(-70)
    t.forward(50)
    t.right(125)
    t.forward(100)
    t.penup()
    t.goto(390,300)
    t.pendown()
    t.left(-180)
    t.forward(100)
    t.left(180)
    t.forward(50)
    t.left(90)
    t.forward(50)
    t.right(90)
    t.forward(50)
    t.left(180)
    t.forward(100)
    t.penup()
    t.goto(455,300)
    t.pendown()
    t.begin_fill()
    t.circle(3)
    t.end_fill()
    t.penup()
    t.goto(460,400)
    t.pendown()
    t.left(180)
    t.forward(100)
    t.left(90)
    t.forward(50)

name()
screen.exitonclick()